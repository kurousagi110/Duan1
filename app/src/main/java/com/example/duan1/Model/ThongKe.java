package com.example.duan1.Model;

public class ThongKe {
    private Integer Thu;
    private Integer Chi;

    public ThongKe() {
    }

    public ThongKe(Integer thu, Integer chi) {
        Thu = thu;
        Chi = chi;
    }

    public Integer getThu() {
        return Thu;
    }

    public void setThu(Integer thu) {
        Thu = thu;
    }

    public Integer getChi() {
        return Chi;
    }

    public void setChi(Integer chi) {
        Chi = chi;
    }
}
